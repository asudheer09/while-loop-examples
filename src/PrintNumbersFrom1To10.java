public class PrintNumbersFrom1To10 {

    public static void main(String[] args) {

        // 1-10

        int i=1;

        while(i<=10){ // 1<=10  --true
            System.out.println(i);
            i++; // i value will be incremented by 1 i=i+1
        }

        System.out.println(12/10);
    }
}
